    return
  }
  
  try {
    const response = await api.post('/api/contactus', formData.value)
    message.value = response.data.message || '✨ Your message has been sent successfully!'
    formData.value = { name: '', email: '', issue_type: '', message: '' }
    
    setTimeout(() => {
      message.value = ''
    }, 5000)
  } catch (error) {
    console.error('Error sending message:', error)
    message.value = error.response?.data?.error || '❌ An error occurred while sending your message. Please try again later.'
    
    setTimeout(() => {
      message.value = ''
    }, 5000)
  }
}

const closeModal = () => {
  openModal.value = false
  queryEmail.value = ''
  queryResults.value = []
  statusMessage.value = ''
  loadingStatus.value = false
}

const checkStatus = async () => {
  if (!queryEmail.value) {
    statusMessage.value = 'Please enter your email address'
    setTimeout(() => {
      statusMessage.value = ''
    }, 3000)
    return
  }

  loadingStatus.value = true
  statusMessage.value = ''
  queryResults.value = []

  try {
    const res = await api.post('/api/query-status', {
      email: queryEmail.value
    })

    if (res.data.queries && res.data.queries.length > 0) {
      queryResults.value = res.data.queries
    } else {
      statusMessage.value = 'No queries found for this email address'
      setTimeout(() => {
        statusMessage.value = ''
      }, 3000)
    }
  } catch (err) {
    console.error(err)
    queryResults.value = []
    statusMessage.value = err.response?.data?.error || 'Something went wrong. Please try again.'
    setTimeout(() => {
      statusMessage.value = ''
    }, 3000)
  } finally {
    loadingStatus.value = false
  }
}

const workingHoursStatus = computed(() => {
  const now = currentTime.value
  const day = now.getDay() // 0 = Sunday, 6 = Saturday
  const minutes = now.getHours() * 60 + now.getMinutes()

  const isWeekday = day >= 1 && day <= 5
  const isSaturday = day === 6

  const openOnWeekday = minutes >= 9 * 60 && minutes < 18 * 60
  const openOnSaturday = minutes >= 10 * 60 && minutes < 16 * 60

  const isOpen = (isWeekday && openOnWeekday) || (isSaturday && openOnSaturday)

  return {
    isOpen,
    label: isOpen ? 'Open Now' : 'Closed Now'
  }
})

onMounted(() => {
  workingHoursTimer = setInterval(() => {
    currentTime.value = new Date()
  }, 60000)
})

onBeforeUnmount(() => {
  if (workingHoursTimer) {
    clearInterval(workingHoursTimer)
  }
})
