export const getCourseBySlug = (slug) => {
  const normalizedSlug = String(slug || '').toLowerCase()
  return (
    courseCatalog.find((course) => {
      const aliases = [course.slug, ...(course.aliases || [])]
      return aliases.some((alias) => String(alias).toLowerCase() === normalizedSlug)
    }) || null
  )
}
